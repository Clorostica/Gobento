const TaskCardWithoutStars: React.FC<{
  children: React.ReactNode;
  className?: string;
  style?: React.CSSProperties;
  task: Task;
  shouldDisableAnimations?: boolean;
  enableTilt?: boolean;
  enableMagnetism?: boolean;
  clickEffect?: boolean;
  glowColor?: string;
}> = ({
  children,
  className = "",
  style,
  task,
  shouldDisableAnimations = false,
  enableTilt = false,
  enableMagnetism = false,
  clickEffect = false,
  glowColor = DEFAULT_GLOW_COLOR,
}) => {
  const cardRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (shouldDisableAnimations || !cardRef.current) return;

    const el = cardRef.current;

    const handleMouseMove = (e: MouseEvent) => {
      if (!enableTilt && !enableMagnetism) return;

      const rect = el.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      const centerX = rect.width / 2;
      const centerY = rect.height / 2;

      if (enableTilt) {
        const rotateX = ((y - centerY) / centerY) * -10;
        const rotateY = ((x - centerX) / centerX) * 10;
        gsap.to(el, {
          rotateX,
          rotateY,
          duration: 0.1,
          ease: "power2.out",
          transformPerspective: 1000,
        });
      }

      if (enableMagnetism) {
        const magnetX = (x - centerX) * 0.05;
        const magnetY = (y - centerY) * 0.05;
        gsap.to(el, {
          x: magnetX,
          y: magnetY,
          duration: 0.3,
          ease: "power2.out",
        });
      }
    };

    const handleMouseLeave = () => {
      if (shouldDisableAnimations) return;

      if (enableTilt) {
        gsap.to(el, {
          rotateX: 0,
          rotateY: 0,
          duration: 0.3,
          ease: "power2.out",
        });
      }

      if (enableMagnetism) {
        gsap.to(el, {
          x: 0,
          y: 0,
          duration: 0.3,
          ease: "power2.out",
        });
      }
    };

    const handleClick = (e: MouseEvent) => {
      if (!clickEffect || shouldDisableAnimations) return;

      const rect = el.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;

      const maxDistance = Math.max(
        Math.hypot(x, y),
        Math.hypot(x - rect.width, y),
        Math.hypot(x, y - rect.height),
        Math.hypot(x - rect.width, y - rect.height)
      );

      const ripple = document.createElement("div");
      ripple.style.cssText = `
        position: absolute;
        width: ${maxDistance * 2}px;
        height: ${maxDistance * 2}px;
        border-radius: 50%;
        background: radial-gradient(circle, rgba(${glowColor}, 0.4) 0%, rgba(${glowColor}, 0.2) 30%, transparent 70%);
        left: ${x - maxDistance}px;
        top: ${y - maxDistance}px;
        pointer-events: none;
        z-index: 1000;
      `;

      el.appendChild(ripple);

      gsap.fromTo(
        ripple,
        {
          scale: 0,
          opacity: 1,
        },
        {
          scale: 1,
          opacity: 0,
          duration: 0.8,
          ease: "power2.out",
          onComplete: () => ripple.remove(),
        }
      );
    };

    el.addEventListener("mousemove", handleMouseMove);
    el.addEventListener("mouseleave", handleMouseLeave);
    el.addEventListener("click", handleClick);

    return () => {
      el.removeEventListener("mousemove", handleMouseMove);
      el.removeEventListener("mouseleave", handleMouseLeave);
      el.removeEventListener("click", handleClick);
    };
  }, [
    shouldDisableAnimations,
    enableTilt,
    enableMagnetism,
    clickEffect,
    glowColor,
  ]);

  return (
    <div ref={cardRef} className={className} style={style}>
      {children}
    </div>
  );
};

const useMobileDetection = () => {
  const [isMobile, setIsMobile] = useState(false);

  useEffect(() => {
    const checkMobile = () =>
      setIsMobile(window.innerWidth <= MOBILE_BREAKPOINT);

    checkMobile();
    window.addEventListener("resize", checkMobile);

    return () => window.removeEventListener("resize", checkMobile);
  }, []);

  return isMobile;
};

const MagicBento: React.FC<BentoProps> = ({
  textAutoHide = true,
  enableStars = true,
  enableSpotlight = true,
  enableBorderGlow = true,
  disableAnimations = false,
  spotlightRadius = DEFAULT_SPOTLIGHT_RADIUS,
  particleCount = DEFAULT_PARTICLE_COUNT,
  enableTilt = false,
  glowColor = DEFAULT_GLOW_COLOR,
  clickEffect = true,
  enableMagnetism = true,
  tasks = [],
  onEdit,
  onDelete,
  onStatusChange,
  addTask,
}) => {
  const gridRef = useRef<HTMLDivElement>(null);
  const isMobile = useMobileDetection();
  const shouldDisableAnimations = disableAnimations || isMobile;
  const [editingId, setEditingId] = useState<string | null>(null);
  const [editText, setEditText] = useState<string>("");

  const handleEditStart = (task: Task) => {
    setEditingId(task.id);
    setEditText(task.text);
  };

  const handleEditSave = (taskId: string) => {
    if (onEdit && editText.trim()) {
      onEdit(taskId, editText.trim());
    }
    setEditingId(null);
    setEditText("");
  };

  const handleEditCancel = () => {
    setEditingId(null);
    setEditText("");
  };

  const handleStatusClick = (task: Task) => {
    if (!onStatusChange) return;
    const statuses: string[] = ["todo", "progress", "completed"];
    const currentIndex = statuses.indexOf(task.status);
    const nextIndex = (currentIndex + 1) % statuses.length;
    const nextStatus = statuses[nextIndex];
    if (nextStatus) {
      onStatusChange(task.id, nextStatus);
    }
  };

  const renderTaskCard = (task: Task, index: number) => {
    const baseClassName = `magic-bento-card ${
      textAutoHide ? "magic-bento-card--text-autohide" : ""
    } ${enableBorderGlow ? "magic-bento-card--border-glow" : ""}`;

    const taskColor = getStatusColor(task.status);
    const cardProps = {
      className: baseClassName,
      style: {
        backgroundColor: taskColor,
        "--glow-color": glowColor,
      } as React.CSSProperties,
    };

    const cardContent = (
      <>
        <div className="magic-bento-card__header">
          <div className="magic-bento-card__label">
            {getStatusLabel(task.status)}
          </div>
          <div className="flex gap-2">
            {onDelete && (
              <button
                onClick={(e) => {
                  e.stopPropagation();
                  onDelete(task.id);
                }}
                className="text-white hover:text-red-300 text-xs px-2 py-1 rounded transition-colors"
                title="Delete"
              >
                âœ•
              </button>
            )}
          </div>
        </div>
        <div className="magic-bento-card__content">
          {editingId === task.id ? (
            <div className="space-y-2">
              <textarea
                value={editText}
                onChange={(e) => setEditText(e.target.value)}
                className="w-full bg-black bg-opacity-30 text-white p-2 rounded resize-none focus:outline-none focus:ring-2 focus:ring-purple-400"
                rows={3}
                autoFocus
                onKeyDown={(e) => {
                  if (e.key === "Enter" && e.ctrlKey) {
                    handleEditSave(task.id);
                  } else if (e.key === "Escape") {
                    handleEditCancel();
                  }
                }}
              />
              <div className="flex gap-2">
                <button
                  onClick={() => handleEditSave(task.id)}
                  className="text-xs px-3 py-1 bg-green-500 hover:bg-green-600 rounded transition-colors text-white"
                >
                  Save
                </button>
                <button
                  onClick={handleEditCancel}
                  className="text-xs px-3 py-1 bg-gray-500 hover:bg-gray-600 rounded transition-colors text-white"
                >
                  Cancel
                </button>
              </div>
            </div>
          ) : (
            <>
              <h2
                className="magic-bento-card__title cursor-pointer hover:opacity-80 transition-opacity"
                onClick={() => onEdit && handleEditStart(task)}
                title="Click to edit"
              >
                {task.text || "Empty task"}
              </h2>
              {onStatusChange && (
                <button
                  onClick={(e) => {
                    e.stopPropagation();
                    handleStatusClick(task);
                  }}
                  className="mt-2 text-xs px-3 py-1 bg-white bg-opacity-20 hover:bg-opacity-30 rounded transition-colors text-white"
                  title="Change status"
                >
                  Change Status
                </button>
              )}
            </>
          )}
        </div>
      </>
    );

    if (enableStars) {
      return (
        <ParticleCard
          key={task.id}
          {...cardProps}
          disableAnimations={shouldDisableAnimations}
          particleCount={particleCount}
          glowColor={glowColor}
          enableTilt={enableTilt}
          clickEffect={clickEffect}
          enableMagnetism={enableMagnetism}
        >
          {cardContent}
        </ParticleCard>
      );
    }

    return (
      <TaskCardWithoutStars
        key={task.id}
        {...cardProps}
        task={task}
        shouldDisableAnimations={shouldDisableAnimations}
        enableTilt={enableTilt}
        enableMagnetism={enableMagnetism}
        clickEffect={clickEffect}
        glowColor={glowColor}
      >
        {cardContent}
      </TaskCardWithoutStars>
    );
  };

  return (
    <>
      {enableSpotlight && (
        <GlobalSpotlight
          gridRef={gridRef}
          disableAnimations={shouldDisableAnimations}
          enabled={enableSpotlight}
          spotlightRadius={spotlightRadius}
          glowColor={glowColor}
        />
      )}

      <BentoCardGrid gridRef={gridRef}>
        {tasks.length > 0 ? (
          tasks.map((task, index) => renderTaskCard(task, index))
        ) : (
          <div className="magic-bento-card col-span-full text-center p-8 opacity-50">
            <p>
              No tasks yet.{" "}
              {addTask && (
                <button
                  onClick={() => addTask("todo")}
                  className="text-purple-400 hover:text-purple-300 underline ml-2"
                >
                  Add one!
                </button>
              )}
            </p>
          </div>
        )}
        {addTask && (
          <div
            className="magic-bento-card cursor-pointer flex items-center justify-center hover:opacity-80 transition-opacity border-2 border-dashed border-purple-400"
            onClick={() => addTask("todo")}
            style={
              {
                backgroundColor: "#060010",
                "--glow-color": glowColor,
              } as React.CSSProperties
            }
          >
            <div className="text-center">
              <div className="text-4xl mb-2">+</div>
              <div className="magic-bento-card__title">Add New Task</div>
            </div>
          </div>
        )}
      </BentoCardGrid>
    </>
  );
};

export default MagicBento;
